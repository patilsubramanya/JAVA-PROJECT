    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String name = n.getText();
            String username = un.getText();
            String password = p.getText();
            String phone_number = phno.getText();
            if(!phone_number.equals("") && !name.equals("") && !username.equals("") && !password.equals("")){
                if(phone_number.length()==10){
                    db.DbConnect.st.executeUpdate("insert into users (uname,username,password,phone_number) values('"+name+"','"+username+"','"+password+"','"+phone_number+"')");
                    JOptionPane.showMessageDialog(null, "Registered Successfully");
                    new Login().setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Please enter 10 digit phone number");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please fill all the details");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

